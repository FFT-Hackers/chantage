#ifndef CHANTAGE_TYPES_H
#define CHANTAGE_TYPES_H

#include <stddef.h>
#include <stdint.h>

typedef int8_t      i8;
typedef int16_t     i16;
typedef int32_t     i32;
typedef int64_t     i64;

typedef uint8_t     u8;
typedef uint16_t    u16;
typedef uint32_t    u32;
typedef uint64_t    u64;

typedef u32         usize;
typedef u32         uword;
typedef u64         uoff;

typedef i32         isize;
typedef i32         iword;
typedef i64         ioff;

#endif
